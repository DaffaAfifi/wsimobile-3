void main(List<String> args) {
  int i = 2;
  int ii = 20;

  print("LOOPING PERTAMA");
  while(i <= 20){
    if(i % 2 == 0){
      print("$i - I love coding");
    }
    i++;
  }

  print("LOOPING KEDUA");
  while(ii >= 1){
    if(ii % 2 == 0){
      print("$ii - I will become a mobile developer");
    }
    ii--;
  }
}